class Solution:
    def sampleStats(self, count: list) -> list:
        l, r = 0, len(count) - 1
        while l <= r:
            if count[l] != 0:
                m = l
                break
            l += 1
        while l <= r:
            if count[r] != 0:
                M = r
                break
            r -= 1
        mean = sum([c * i for i, c in enumerate(count)]) / sum(count)
        medInd = sum(count)
        if medInd % 2:
            s = 0
            for ci, c in enumerate(count):
                if s < medInd // 2 + 1 <= s + c:
                    med = ci
                    break
                s += c
        else:
            s = 0
            for ci, c in enumerate(count):
                if s < medInd // 2 <= s + c:
                    med1 = ci
                    break
                s += c
            s = 0
            for ci, c in enumerate(count[::-1]):
                if s < medInd // 2 <= s + c:
                    med2 = len(count) - 1 - ci
                    break
                s += c
            med = (med1 + med2) / 2
        mode = count.index(max(count))
        return list(map(float, [m, M, mean, med, mode]))


